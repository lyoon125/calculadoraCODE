    /* Metodo para borrar los decimales innecesarios
    
    */
    public String enteros (float resultado){
        String retorno ="";
        retorno = Float.toString(resultado);
        if(resultado%1==0){
            retorno = retorno.substring(0, retorno.length()-2);
        }
        return retorno;
    }
    
